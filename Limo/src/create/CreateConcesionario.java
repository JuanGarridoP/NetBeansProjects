        String date = df.format(fechaNac.getDate());
        if(nombre.getText().equals("") ||  apellidoP.getText().equals("") ||  apellidoM.getText().equals("") ||  date.equals("") ||  direccion.getText().equals("") ||  telefono.getText().equals("")){
            JOptionPane.showMessageDialog (null, "Completa la informaci贸n por favor", "Atenci贸n!!", JOptionPane.INFORMATION_MESSAGE);
 
        }else{
            exec= concesionarios.conInsert(nombre.getText(), apellidoP.getText(), apellidoM.getText(),generoBox.getSelectedItem().toString(),date, direccion.getText(),Integer.parseInt(telefono.getText()));
            if(exec !=true){
                JOptionPane.showMessageDialog (null, "Parece que ha sucedido un problema", "Atenci贸n!!", JOptionPane.ERROR_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog (null, "Se ha agregado correctamente!", "", JOptionPane.INFORMATION_MESSAGE);
                try {
                    this.setClosed(true);
                } catch (PropertyVetoException ex) {
                    Logger.getLogger(CreateUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
